# Using Variables in Strings

# Assign the first name to the variable 'first_name'
first_name = "ada"

# Assign the last name to the variable 'last_name'
last_name = "lovelace"

# Combine first and last name into 'full_name' using an f-string
full_name = f"{first_name} {last_name}"

# Print the full name
print(full_name)

# Print a greeting using the full name with proper capitalization
print(f"Hello, {full_name.title()}!")

# Assign a greeting message to the variable 'message'
message = f"Hello, {full_name.title()}!"

# Print the greeting message
print(message)







