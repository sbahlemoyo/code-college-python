# Changing Case in a String with Methods

# Assign the string "ada lovelace" to the variable 'name'
name = "ada lovelace"

# Print the string with the first letter of each word capitalized
print(name.title())

# Assign the string "Ada Lovelace" to the variable 'name'
name = "Ada Lovelace"

# Print the string in all uppercase letters
print(name.upper())

# Print the string in all lowercase letters
print(name.lower())




